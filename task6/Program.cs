// Напишите программу, которая принимает на вход цифру, обозначающую день недели, и проверяет, является ли этот день недели выходным
Console.Write("Введите номер дня недели (1-7): ");
string input = Console.ReadLine();
int днинедели;
if (input.Length == 1 && input[0] >= '1' && input[0] <= '7')
{
днинедели = input[0] - '0';
if (днинедели == 6 || днинедели == 7)
{
Console.WriteLine("Это выходной день.");
}
else
{
Console.WriteLine("Это будний день.");
}
}
else
{
Console.WriteLine("Такого дня недели нет. Пожалуйста, введите число от 1 до 7.");
}
